x = 16
y = 5
age = 18

# Penjumlahan
print("Penjumlahan nilai:", x, "+", y, "=", x + y)

# Pengurangan
print("Pengurangan nilai:", x, "-", y, "=", x - y)

# Perkalian
print("Perkalian nilai:", x, "*", y, "=", x * y)

# Pembagian
print("Pembagian nilai:", x, "/", y, "=", x / y)

# Sisa nilai pembagian / modulus
print("Sisa pembagian nilai:", x, "%", y, "=", x % y)

print("Umur saya sekarang", age, "tahun")
print("Tahun 2020 umur saya bertambah menjadi", age + 1, "tahun")
